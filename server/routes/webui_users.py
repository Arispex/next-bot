from __future__ import annotations

import re
from dataclasses import dataclass
from datetime import datetime
from typing import Any

from fastapi import APIRouter, Request
from fastapi.responses import JSONResponse

from next_bot.db import Group, Server, User, get_session
from next_bot.tshock_api import (
    TShockRequestError,
    get_error_reason,
    is_success,
    request_server_api,
)

router = APIRouter()

_USER_ID_PATTERN = re.compile(r"^\d{5,20}$")
_USER_NAME_PATTERN = re.compile(r"^[A-Za-z0-9\u4e00-\u9fff]+$")
_MAX_USER_NAME_LENGTH = 16


@dataclass(frozen=True)
class ValidatedUserPayload:
    user_id: str
    name: str
    coins: int
    permissions: str
    group: str


class UserPayloadValidationError(ValueError):
    def __init__(self, message: str, *, field: str | None = None):
        super().__init__(message)
        self.field = field


def _bad_request(message: str) -> JSONResponse:
    return JSONResponse(status_code=400, content={"ok": False, "message": message})


def _unprocessable(message: str, *, field: str | None = None) -> JSONResponse:
    content: dict[str, Any] = {"ok": False, "message": message}
    if field:
        content["field"] = field
    return JSONResponse(status_code=422, content=content)


def _not_found(message: str) -> JSONResponse:
    return JSONResponse(status_code=404, content={"ok": False, "message": message})


def _internal_error(message: str) -> JSONResponse:
    return JSONResponse(status_code=500, content={"ok": False, "message": message})


def _require_field(payload: dict[str, Any], key: str) -> Any:
    if key not in payload:
        raise UserPayloadValidationError(f"{key} 为必填项", field=key)
    return payload.get(key)


def _normalize_user_id(raw_value: Any) -> str:
    value = str(raw_value).strip()
    if not value:
        raise UserPayloadValidationError("用户ID不能为空", field="user_id")
    if _USER_ID_PATTERN.fullmatch(value) is None:
        raise UserPayloadValidationError("用户ID必须是 5-20 位数字", field="user_id")
    return value


def _normalize_user_name(raw_value: Any) -> str:
    value = str(raw_value).strip()
    if not value:
        raise UserPayloadValidationError("用户名称不能为空", field="name")
    if len(value) > _MAX_USER_NAME_LENGTH:
        raise UserPayloadValidationError(
            f"用户名称过长，最多 {_MAX_USER_NAME_LENGTH} 个字符",
            field="name",
        )
    if value.isdigit():
        raise UserPayloadValidationError("用户名称不能为纯数字", field="name")
    if _USER_NAME_PATTERN.fullmatch(value) is None:
        raise UserPayloadValidationError(
            "用户名称不能包含符号，只能使用中文、英文和数字",
            field="name",
        )
    return value


def _normalize_coins(raw_value: Any) -> int:
    if isinstance(raw_value, bool):
        raise UserPayloadValidationError("金币必须是非负整数", field="coins")

    parsed: int
    if isinstance(raw_value, int):
        parsed = raw_value
    elif isinstance(raw_value, float):
        if not raw_value.is_integer():
            raise UserPayloadValidationError("金币必须是整数", field="coins")
        parsed = int(raw_value)
    elif isinstance(raw_value, str):
        text = raw_value.strip()
        if not text:
            raise UserPayloadValidationError("金币不能为空", field="coins")
        try:
            parsed = int(text)
        except ValueError as exc:
            raise UserPayloadValidationError("金币必须是整数", field="coins") from exc
    else:
        raise UserPayloadValidationError("金币必须是整数", field="coins")

    if parsed < 0:
        raise UserPayloadValidationError("金币必须是非负整数", field="coins")
    return parsed


def _normalize_permissions(raw_value: Any) -> str:
    if raw_value is None:
        return ""
    text = str(raw_value).strip()
    if not text:
        return ""
    values = sorted({item.strip() for item in text.split(",") if item.strip()})
    return ",".join(values)


def _normalize_group(raw_value: Any) -> str:
    value = str(raw_value).strip()
    if not value:
        raise UserPayloadValidationError("身份组不能为空", field="group")
    return value


def _validate_payload(payload: dict[str, Any]) -> ValidatedUserPayload:
    user_id = _normalize_user_id(_require_field(payload, "user_id"))
    name = _normalize_user_name(_require_field(payload, "name"))
    coins = _normalize_coins(_require_field(payload, "coins"))
    permissions = _normalize_permissions(payload.get("permissions", ""))
    group = _normalize_group(_require_field(payload, "group"))
    return ValidatedUserPayload(
        user_id=user_id,
        name=name,
        coins=coins,
        permissions=permissions,
        group=group,
    )


def _format_created_at(value: datetime | None) -> str:
    if value is None:
        return ""
    return value.strftime("%Y-%m-%d %H:%M:%S")


def _serialize_user(user: User) -> dict[str, Any]:
    return {
        "id": int(user.id),
        "user_id": str(user.user_id),
        "name": str(user.name),
        "coins": int(user.coins),
        "permissions": str(user.permissions or ""),
        "group": str(user.group),
        "created_at": _format_created_at(user.created_at),
    }


async def _sync_user_whitelist(user: User) -> list[dict[str, Any]]:
    session = get_session()
    try:
        servers = session.query(Server).order_by(Server.id.asc()).all()
    finally:
        session.close()

    results: list[dict[str, Any]] = []
    for server in servers:
        try:
            response = await request_server_api(
                server,
                "/v3/server/rawcmd",
                params={"cmd": f"/bwl add {user.name}"},
            )
        except TShockRequestError:
            results.append(
                {
                    "server_id": int(server.id),
                    "server_name": str(server.name),
                    "success": False,
                    "reason": "无法连接服务器",
                }
            )
            continue

        if is_success(response):
            results.append(
                {
                    "server_id": int(server.id),
                    "server_name": str(server.name),
                    "success": True,
                    "reason": "",
                }
            )
            continue

        results.append(
            {
                "server_id": int(server.id),
                "server_name": str(server.name),
                "success": False,
                "reason": get_error_reason(response),
            }
        )
    return results


@router.get("/webui/api/users")
async def webui_users_list() -> JSONResponse:
    session = get_session()
    try:
        users = session.query(User).order_by(User.id.asc()).all()
        groups = session.query(Group).order_by(Group.name.asc()).all()
        return JSONResponse(
            content={
                "ok": True,
                "users": [_serialize_user(item) for item in users],
                "groups": [str(item.name) for item in groups],
            }
        )
    except Exception as exc:
        return _internal_error(f"获取用户列表失败：{exc}")
    finally:
        session.close()


@router.post("/webui/api/users")
async def webui_users_create(request: Request) -> JSONResponse:
    try:
        payload = await request.json()
    except Exception:
        return _bad_request("请求体必须是 JSON")
    if not isinstance(payload, dict):
        return _bad_request("请求体必须是对象")

    try:
        validated = _validate_payload(payload)
    except UserPayloadValidationError as exc:
        return _unprocessable(str(exc), field=exc.field)

    session = get_session()
    try:
        if (
            session.query(User).filter(User.user_id == validated.user_id).first()
            is not None
        ):
            return _unprocessable("用户ID已存在", field="user_id")

        if session.query(User).filter(User.name == validated.name).first() is not None:
            return _unprocessable("用户名称已被占用", field="name")

        if session.query(Group).filter(Group.name == validated.group).first() is None:
            return _unprocessable("身份组不存在", field="group")

        user = User(
            user_id=validated.user_id,
            name=validated.name,
            coins=validated.coins,
            permissions=validated.permissions,
            group=validated.group,
        )
        session.add(user)
        session.commit()
        return JSONResponse(
            content={
                "ok": True,
                "message": "新增成功",
                "user": _serialize_user(user),
            }
        )
    except Exception as exc:
        session.rollback()
        return _internal_error(f"新增用户失败：{exc}")
    finally:
        session.close()


@router.put("/webui/api/users/{id}")
async def webui_users_update(id: int, request: Request) -> JSONResponse:
    try:
        payload = await request.json()
    except Exception:
        return _bad_request("请求体必须是 JSON")
    if not isinstance(payload, dict):
        return _bad_request("请求体必须是对象")

    try:
        validated = _validate_payload(payload)
    except UserPayloadValidationError as exc:
        return _unprocessable(str(exc), field=exc.field)

    session = get_session()
    try:
        user = session.query(User).filter(User.id == id).first()
        if user is None:
            return _not_found("更新失败，用户不存在")

        if (
            session.query(User)
            .filter(User.user_id == validated.user_id, User.id != id)
            .first()
            is not None
        ):
            return _unprocessable("用户ID已存在", field="user_id")

        if (
            session.query(User)
            .filter(User.name == validated.name, User.id != id)
            .first()
            is not None
        ):
            return _unprocessable("用户名称已被占用", field="name")

        if session.query(Group).filter(Group.name == validated.group).first() is None:
            return _unprocessable("身份组不存在", field="group")

        user.user_id = validated.user_id
        user.name = validated.name
        user.coins = validated.coins
        user.permissions = validated.permissions
        user.group = validated.group
        session.commit()
        return JSONResponse(
            content={
                "ok": True,
                "message": "更新成功",
                "user": _serialize_user(user),
            }
        )
    except Exception as exc:
        session.rollback()
        return _internal_error(f"更新用户失败：{exc}")
    finally:
        session.close()


@router.delete("/webui/api/users/{id}")
async def webui_users_delete(id: int) -> JSONResponse:
    session = get_session()
    try:
        user = session.query(User).filter(User.id == id).first()
        if user is None:
            return _not_found("删除失败，用户不存在")

        session.delete(user)
        session.commit()
        return JSONResponse(content={"ok": True, "message": "删除成功"})
    except Exception as exc:
        session.rollback()
        return _internal_error(f"删除用户失败：{exc}")
    finally:
        session.close()


@router.post("/webui/api/users/{id}/sync-whitelist")
async def webui_users_sync_whitelist(id: int) -> JSONResponse:
    session = get_session()
    try:
        user = session.query(User).filter(User.id == id).first()
    except Exception as exc:
        return _internal_error(f"读取用户失败：{exc}")
    finally:
        session.close()

    if user is None:
        return _not_found("同步失败，用户不存在")

    try:
        results = await _sync_user_whitelist(user)
    except Exception as exc:
        return _internal_error(f"同步白名单失败：{exc}")

    message = "同步完成"
    if not results:
        message = "同步失败，暂无可同步的服务器"

    return JSONResponse(
        content={
            "ok": True,
            "data": {
                "user_id": str(user.user_id),
                "name": str(user.name),
                "message": message,
                "results": results,
            },
        }
    )
